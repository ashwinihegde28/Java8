package org.example.inheritance;

public class Address {
    String streetName;
    String StreetNo;
    String city;
    String postalCode;

    public Address(String streetName, String streetNo, String city, String postalCode) {
        this.streetName = streetName;
        StreetNo = streetNo;
        this.city = city;
        this.postalCode = postalCode;
    }
}
